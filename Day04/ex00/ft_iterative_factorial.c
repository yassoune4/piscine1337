int	ft_iterative_factorial(int nb)
{
	int res;
	int i;

	i = 2;
	res = 1;
	if (nb >= 0 && nb <= 12)
	{
		while (i <= nb)
		{
			res *= i;
			i++;
		}
	}
	else
	{
		return (0);
	}
	return (res);
}
